    // ───────────────────────────────────────────────
    //  EditorWindow GUI
    // ───────────────────────────────────────────────
    private void OnGUI()
    {
        serializedObject.Update();

        GUILayout.Label("Maze Generator", EditorStyles.boldLabel);
        EditorGUILayout.Space();

        EditorGUILayout.PropertyField(
            serializedObject.FindProperty("wallPrefab"),
            new GUIContent("Wall Prefab", "직선 벽 프리팹"));

        serializedObject.ApplyModifiedProperties();

        EditorGUILayout.Space();

        if (GUILayout.Button("Generate Maze", GUILayout.Height(40)))
        {
            GenerateMaze();
        }
    }

    // ───────────────────────────────────────────────
    //  헬퍼: 맵 셀 값 반환 (범위 밖 → 0)
    // ───────────────────────────────────────────────
    private int GetCell(int row, int col)
    {
        if (row < 0 || row >= mapRows.Length) return 0;
        if (col < 0 || col >= mapRows[row].Length) return 0;
        return mapRows[row][col] == '1' ? 1 : 0;
    }

    // ───────────────────────────────────────────────
    //  헬퍼: 프리팹 인스턴스 생성
    // ───────────────────────────────────────────────
    private void SpawnPrefab(GameObject prefab, Transform parent,
                             Vector3 worldPos, Vector3 euler, string objName)
    {
        GameObject obj = (GameObject)PrefabUtility.InstantiatePrefab(prefab);
        obj.name = objName;
        obj.transform.SetParent(parent);
        obj.transform.position = worldPos;
        obj.transform.eulerAngles = euler;
        Undo.RegisterCreatedObjectUndo(obj, "Generate Maze");
    }

    // ───────────────────────────────────────────────
    //  메인: 미로 생성
    // ───────────────────────────────────────────────
    private void GenerateMaze()
    {
        // 기존 MazeRoot 삭제
        GameObject existing = GameObject.Find("MazeRoot");
        if (existing != null)
            Undo.DestroyObjectImmediate(existing);

        // 새 MazeRoot 생성
        GameObject mazeRoot = new GameObject("MazeRoot");
        mazeRoot.transform.position = Vector3.zero;
        Undo.RegisterCreatedObjectUndo(mazeRoot, "Generate Maze");
        Transform root = mazeRoot.transform;

        int wallCount = 0;
        int wallSkip  = 0;

        int rows = mapRows.Length;
        for (int row = 0; row < rows; row++)
        {
            int cols = mapRows[row].Length;
            for (int col = 0; col < cols; col++)
            {
                if (GetCell(row, col) == 0) continue;

                // 월드 기준 좌표 (바닥 1칸 = 3×3 유닛)
                Vector3 basePos = new Vector3(col * 3f, 0f, row * 3f);

                // ── 4방향 (0 = 막힘) ──
                bool top    = GetCell(row - 1, col) == 0;
                bool bottom = GetCell(row + 1, col) == 0;
                bool left   = GetCell(row, col - 1) == 0;
                bool right  = GetCell(row, col + 1) == 0;

                // 왼쪽(-X) 벽
                if (left)
                {
                    if (wallPrefab != null)
                    {
                        SpawnPrefab(wallPrefab, root,
                            basePos + new Vector3(0f, 0f, 0f),
                            new Vector3(0f, 0f, 0f),
                            $"Wall_{row}_{col}_L");
                        wallCount++;
                    }
                    else wallSkip++;
                }

                // 오른쪽(+X) 벽
                if (right)
                {
                    if (wallPrefab != null)
                    {
                        SpawnPrefab(wallPrefab, root,
                            basePos + new Vector3(3f, 0f, 3f),
                            new Vector3(0f, 180f, 0f),
                            $"Wall_{row}_{col}_R");
                        wallCount++;
                    }
                    else wallSkip++;
                }

                // 위쪽(-Z) 벽
                if (top)
                {
                    if (wallPrefab != null)
                    {
                        SpawnPrefab(wallPrefab, root,
                            basePos + new Vector3(3f, 0f, 0f),
                            new Vector3(0f, -90f, 0f),
                            $"Wall_{row}_{col}_T");
                        wallCount++;
                    }
                    else wallSkip++;
                }

                // 아래쪽(+Z) 벽
                if (bottom)
                {
                    if (wallPrefab != null)
                    {
                        SpawnPrefab(wallPrefab, root,
                            basePos + new Vector3(0f, 0f, 3f),
                            new Vector3(0f, 90f, 0f),
                            $"Wall_{row}_{col}_B");
                        wallCount++;
                    }
                    else wallSkip++;
                }
            }
        }

        if (wallSkip > 0) Debug.LogWarning($"[MazeGenerator] wallPrefab 미할당 — {wallSkip}개 건너뜀");
        Debug.Log($"[MazeGenerator] 생성 완료 — 직선 벽: {wallCount}개");

        EditorUtility.DisplayDialog(
            "Maze Generator",
            $"미로 생성 완료!\n\n직선 벽: {wallCount}개",
            "확인");
    }
